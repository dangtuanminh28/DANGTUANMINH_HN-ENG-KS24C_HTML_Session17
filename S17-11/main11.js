// Math.abs() - Giá trị tuyệt đối (luôn dương)
console.log(Math.abs(-5.5)); // 5.5
console.log(Math.abs(10));   // 10 (không đổi)

// Math.ceil() - Làm tròn lên
console.log(Math.ceil(4.4)); // 5 (lên số nguyên gần nhất)
console.log(Math.ceil(5.8)); // 6

// Math.floor() - Làm tròn xuống
console.log(Math.floor(5.7)); // 5 (xuống số nguyên gần nhất)
console.log(Math.floor(6.1)); // 6

// Math.round() - Làm tròn theo quy tắc (>= .5 lên, < .5 xuống)
console.log(Math.round(10.1)); // 10
console.log(Math.round(20.6)); // 21
